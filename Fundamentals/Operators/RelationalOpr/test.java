package Operators.RelationalOpr;

public class test {
    public static void main(String[] args) {
        //Relational Operators
        int a = 10;
        int b = 20;
        System.out.println("a==b: " + (a == b)); // 10==20 = false
        System.out.println("a!=b: " + (a != b)); // 10!=20 = true
        System.out.println("a>b: " + (a > b)); // 10>20 = false
        System.out.println("a<b: " + (a < b)); // 10<20 = true
        System.out.println("a>=b: " + (a >= b)); // 10>=20 = false
        System.out.println("a<=b: " + (a <= b)); // 10<=20 = true
        
    }
}
